document
  .getElementById("btn-complete-1")
  .addEventListener("click", function (event) {
    event.preventDefault();
    event.target.setAttribute("disabled", true);
    const accumulatedAmount =
      document.getElementById("accumulated-amount").innerText;
    const fullAccumulatedAmount = parseInt(accumulatedAmount);
    const taskDecrease = document.getElementById("task-decrease").innerText;
    const fullTaskDecrease = parseInt(taskDecrease);
    const sub = fullTaskDecrease - 1;
    const sum = fullAccumulatedAmount + 1;
    document.getElementById("task-decrease").innerText = sub;
    document.getElementById("accumulated-amount").innerText = sum;
    alert("Board Updated Successfully");
    const activityWork = document.getElementById("activity-work");
    const title1 = document.getElementById('title-1').innerText;
    const p = document.createElement("p");
    const now = new Date();
    const hours = now.getHours();
    const minutes = now.getMinutes();
    const seconds = now.getSeconds();
    p.innerText = `
    You have Complete The Task ${title1} at ${hours} : ${minutes}: ${seconds};
    `;
    activityWork.appendChild(p);
  });
